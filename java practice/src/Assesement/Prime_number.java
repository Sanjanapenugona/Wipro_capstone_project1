package Assesement;

public class Prime_number {

	public static void main(String[] args) {
		int num=1;
		for(int i=1; i<=num; i++) {
			if(num%i==0) {
			System.out.println("prime number");
		    }
			else
			{
				System.out.println("not prime number");
				return;
			}
	
		}
			
		

	}

}
